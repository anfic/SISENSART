    private void Detallar() 
    {
        try 
        {
            for(int i=0;i<TBAdherir.getRowCount();i++)        
            {
                if(ctrl.ValorTabla(TBAdherir, 5).toString().trim().length()==0 || ctrl.ValorTabla(TBAdherir, 6).toString().trim().length()==0)
                {
                    ctrl.mensaje.MensajeAlerta("Valores vacios");                    
                }
                else
                {
                    String orden=IFRM_ORDEN_COMPRA.TXorden.getText().trim();
                    ctrl.GrabarData("detalle_orden", "id_material,id_orden,cantidad,p_unitario,p_total,asiganado,unidad", "'"+TBAdherir.getValueAt(i, 0).toString().trim()+"'"
                            + ",'"+orden+"','"+TBAdherir.getValueAt(i, 3).toString().trim()+"','"+TBAdherir.getValueAt(i, 5).toString().trim()+"','"+TBAdherir.getValueAt(i, 6).toString().trim()+"',2,'"+TBAdherir.getValueAt(i, 4).toString().trim()+"'");
                }
            }            
        } catch (NullPointerException e)
        {
            ctrl.mensaje.MensajeAlerta("Valores nulos:\n"+e.getMessage());
        }
        
    }
