    private void Cambiar() 
    {
        if (TBrequerimiento.getRowCount()==0)
        {
            ctrl.mensaje.MensajeAlerta("No hay ningun Requerimiento que seleccionar.");
            this.dispose();
        }
        else if(TBrequerimiento.getSelectedRow()==-1)
        {
            ctrl.mensaje.MensajeAlerta("No seleccionó ningún requerimiento");
        }
        else if(TBrequerimiento.getSelectedRowCount()>1)
        {
            ctrl.mensaje.MensajeAlerta("seleccione solo un item.");
        }
        else
        {
            ctrl.ActualizarData("requerimiento", "estado='7'", "id_requerimiento='"+ctrl.ValorTabla(TBrequerimiento, 0)+"'");
            Ver();
        }
    }
