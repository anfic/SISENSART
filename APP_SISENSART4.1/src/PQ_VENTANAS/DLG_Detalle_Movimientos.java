    private void ADICIONAR() {
        if (TBmaterial.getSelectedRow() == -1) {
            ctrl.mensaje.MensajeAlerta("Ud. no seleccionó ningún material.");
            ctrl.Maracar(TXbuscar);
        } else if (TXcantidad.getText().trim().length() == 0) {
            ctrl.mensaje.MensajeAlerta("Ud. no ingresó una cantidad.");
            TXcantidad.requestFocus();
        } else if (CBunidad.getSelectedIndex() == -1) {
            ctrl.mensaje.MensajeAlerta("Ud. no seleccionó ninguna unidad de inghreso.");
            CBunidad.requestFocus();
        } else if (CBalmacen.getSelectedIndex() == -1) {
            ctrl.mensaje.MensajeAlerta("Ud. no seleccionó ningún almacen.");
            CBalmacen.requestFocus();
        } else if (ctrl.VerificarRepiteEnTabla(TBdetalle, TBmaterial, 2, 1, CBalmacen) == true) {
            ctrl.mensaje.MensajeAlerta("El artículo " + TBmaterial.getValueAt(TBmaterial.getSelectedRow(), 1).toString() + ".\nYa se encuentra registrado, porfavor verificar."
                    + "\n\nModifique cantidades O\nQuite el artículo y vuelva a insertar.");
            Limpiar();
        } else {
            int registros = TBdetalle.getRowCount();
            String a, b, c, d, e, f, g, h,i;
            a = LBcodigo.getText().trim();
            b = ctrl.ValorTabla(TBmaterial, 0);
            c = ctrl.ValorTabla(TBmaterial, 1);
            d = TXcantidad.getText().trim();
            e = TXpeso.getText().trim();
            f = CBunidad.getSelectedItem().toString().trim();
            g = ctrl.ObtenerCodigo("almacen", "descripcion", CBalmacen.getSelectedItem().toString().trim(), 1);
            h = TXobservacion.getText().trim();
            i = String.valueOf(Double.parseDouble(ctrl.ValorTabla(TBmaterial, 2))* Double.parseDouble(d));
            String[] datos = {a, b, c, f, d, e, h, g,i};
            ctrl.dtm = (DefaultTableModel) TBdetalle.getModel();
            ctrl.dtm.addRow(datos);
            Limpiar();
        }
    }

    private void Confirmar() {
        PreparedStatement stmt = null;
        PreparedStatement stmt2 = null;
        try {
            ctrl.Base.conec.setAutoCommit(false);
            stmt = ctrl.Base.conec.prepareStatement("insert into detalle_movimiento values(?,?,?,?,?,?,?,?);");
            stmt2 = ctrl.Base.conec.prepareStatement("update movimiento set estado=? where id_movimiento=?;");
            //Transaccion de detalle de movimiento
            for (int i = 0; i < TBdetalle.getRowCount(); i++) {
                stmt.setString(1, TBdetalle.getValueAt(i, 1).toString().trim());
                stmt.setString(2, TBdetalle.getValueAt(i, 0).toString().trim());
                stmt.setString(3, TBdetalle.getValueAt(i, 4).toString().trim());
                stmt.setString(4, TBdetalle.getValueAt(i, 5).toString().trim());
                stmt.setString(5, TBdetalle.getValueAt(i, 6).toString().trim());
                stmt.setString(6, TBdetalle.getValueAt(i, 7).toString().trim());
                stmt.setString(7, TBdetalle.getValueAt(i, 3).toString().trim());
                stmt.setString(8, TBdetalle.getValueAt(i, 8).toString().trim());
                stmt.executeUpdate();
            }
            //Transaccion de movimiento
            stmt2.setString(1, "2");
            stmt2.setString(2, LBcodigo.getText().trim());
            stmt2.executeUpdate();
            ctrl.Base.conec.commit();
            ctrl.mensaje.MensajeSimple("Transacción de movimiento finalizado.");
            this.dispose();
            reporte.Reporte_1Parametro_Imagen("RPT_Movimiento.jasper", "p_id", LBcodigo.getText().trim(), "p_logo", "/PQ_IMAGENES/LogoSantisimaCruzEdit.png");
            FRM_Movimiento_Materiales.BTcancelar.doClick();
        } catch (SQLException e) {
            System.err.println("ERROR: " + e.getMessage());
            if (ctrl.Base.conec != null) {
                System.out.println("Rollback");
                try {
                    ctrl.Base.conec.rollback();
                } catch (SQLException ex) {
                    System.err.println("No se pudo deshacer" + ex.getMessage());
                }
            }
        } 
    }
