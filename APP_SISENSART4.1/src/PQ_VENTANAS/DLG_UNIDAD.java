    private void Guardar()
    {
        if(TXdescripcion.getText().trim().length()==0)
        {
            ctrl.mensaje.MensajeError("Ud. no ingreso la descripción de la unidad de medida.");
            TXdescripcion.requestFocus();
        }
        else if(TXabreviatura.getText().trim().length()==0)
        {
            ctrl.mensaje.MensajeError("Ud. no ingreso la abreviatura de la unidad de medida.");
            TXabreviatura.requestFocus();
        }
        else
        {
            ctrl.GrabarData("unidad", "descripcion,abreviatura", "'"+TXdescripcion.getText().trim()+"','"+TXabreviatura.getText().trim()+"'");
            Cancelar();
        }
    }
    private void Cancelar()
    {
        TXdescripcion.setText("");
        TXabreviatura.setText("");
        TXdescripcion.requestFocus();
    }

