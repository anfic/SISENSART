    private void Procesar() {
        if(TBrequerimiento.getRowCount()==0)
        {
            ctrl.mensaje.MensajeAlerta("No hay nada para procesar");
            ctrl.Maracar(TXbuscar);
        }
        else if(TBrequerimiento.getSelectedRow()==-1)
        {
            ctrl.mensaje.MensajeAlerta("Ud. no seleccionÃ³ ninguna fila");
            ctrl.Maracar(TXbuscar);
        }
        else if(TBrequerimiento.getSelectedRowCount()>1)
        {
            ctrl.mensaje.MensajeAlerta("No seleccione mÃ¡s de una fila.");
            ctrl.Maracar(TXbuscar);
        }
        else
        {
            if(ctrl.ValorTabla(TBrequerimiento, 3).equalsIgnoreCase("EN PROCESO"))
            {
                String id,sol,ob;
                id=ctrl.ValorTabla(TBrequerimiento, 0);
                sol=ctrl.ValorTabla(TBrequerimiento, 5);
                ob=ctrl.ValorTabla(TBrequerimiento, 4);
                IFRM_ORDEN_COMPRA.TXrequerimiento.setText(id);
                IFRM_ORDEN_COMPRA.TXsolicitante.setText(sol);
                IFRM_ORDEN_COMPRA.TXobra.setText(ob);
            }
            else 
            {
                if(ctrl.mensaje.MsjeConfirmacion("El requerimiento Nro. "+ctrl.ValorTabla(TBrequerimiento, 0)+", cambiarÃ¡ de estado a: EN PROCESO\nEstÃ¡ seguro que desea continuar?")==0)
                {
                    String id,sol,ob;
                    id=ctrl.ValorTabla(TBrequerimiento, 0);
                    sol=ctrl.ValorTabla(TBrequerimiento, 5);
                    ob=ctrl.ValorTabla(TBrequerimiento, 4);
                    ctrl.ActualizarData("requerimiento", "estado=7", "id_requerimiento='"+ctrl.ValorTabla(TBrequerimiento, 0)+"'");
                    IFRM_ORDEN_COMPRA.TXrequerimiento.setText(id);
                    IFRM_ORDEN_COMPRA.TXsolicitante.setText(sol);
                    IFRM_ORDEN_COMPRA.TXobra.setText(ob);
                }
                else
                {
                    ctrl.mensaje.MsjeConfirmacion("Proceso Cancelado");
                    this.dispose();
                }
            }
        }
    }
    private void Atendido()
    {
        if (TBrequerimiento.getRowCount()==0)
        {
            ctrl.mensaje.MensajeAlerta("No hay nada para atender");
        }
        else if(TBrequerimiento.getSelectedRow()==-1)
        {
            ctrl.mensaje.MensajeAlerta("Ud. no seleccionÃ³ ningÃºn item.");
        }
        else if(TBrequerimiento.getSelectedRowCount()>1)
        {
            ctrl.mensaje.MensajeAlerta("Seleccione solo un item.");
        }
        else
        {
            if(ctrl.mensaje.MsjeConfirmacion("Este requerimiento pasara al estado de ATENDIDO.\nDesea continuar?")==0)
            {
                ctrl.ActualizarData("requerimiento", "estado=8", "id_requerimiento='"+ctrl.ValorTabla(TBrequerimiento, 0)+"'");
                Buscar();
            }
            else
            {
                ctrl.mensaje.MsjeConfirmacion("Proceso Cancelado");
                Buscar();
            }
        }
        
    }
