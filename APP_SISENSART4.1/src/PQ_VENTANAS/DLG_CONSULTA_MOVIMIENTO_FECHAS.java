    private void Consultar() {
        if (DT1.getDate() == null) {
            ctrl.mensaje.MensajeAlerta("Ud. no ingresó la fecha DESDE.");
        } else if (DT2.getDate() == null) {
            ctrl.mensaje.MensajeAlerta("Ud. no ingresó la fecha HASTA.");
        } else {
            Visualizar("where (movimiento_fecha BETWEEN '" + ctrl.DevolverFechaJDateChooser(DT1) + "' AND '" + ctrl.DevolverFechaJDateChooser(DT2) + "')and movimiento_tipo_movimiento='" + CBtipo.getSelectedItem().toString().trim() + "' order by movimiento_id_movimiento");
        }
    }
