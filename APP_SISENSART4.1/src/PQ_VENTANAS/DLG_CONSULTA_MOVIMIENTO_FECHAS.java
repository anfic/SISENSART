    private void BTexcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTexcelActionPerformed
        // TODO add your handling code here:
        if (TBdetalle.getRowCount() == 0) {
            ctrl.mensaje.MensajeAlerta("No hay ningun elemento para exportar.");
        } else {
            JFileChooser seleccionar = new JFileChooser();
            File archivo;
            if (seleccionar.showDialog(null, "Exportar Excel") == JFileChooser.APPROVE_OPTION) {
                archivo = seleccionar.getSelectedFile();
                int cantFila = TBdetalle.getRowCount();
                int cantColumna = TBdetalle.getColumnCount();
                Workbook wb;
                wb = new XSSFWorkbook();
                Sheet hoja = wb.createSheet("  ");

                try {
                    for (int i = -1; i < cantFila; i++) {
                        Row fila = hoja.createRow(i + 1);
                        for (int j = 0; j < cantColumna; j++) {
                            Cell celda = fila.createCell(j);
                            if (i == -1) {
                                celda.setCellValue(String.valueOf(TBdetalle.getColumnName(j)));
                            } else {
                                celda.setCellValue(String.valueOf(TBdetalle.getValueAt(i, j)));
                            }
                            wb.write(new FileOutputStream(archivo + ".xlsx"));
                        }
                    }
                    JOptionPane.showMessageDialog(null, "Exportacion exitosa");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Vuelve a intentarlo");
                }

            } else {

            }
        }
    }//GEN-LAST:event_BTexcelActionPerformed

