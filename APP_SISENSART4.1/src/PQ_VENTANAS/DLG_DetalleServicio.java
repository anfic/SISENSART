    private void Adicionar() {
        if(TXdescripcion.getText().trim().length()==0){
            ctrl.mensaje.MensajeAlerta("Registre el servicio.");
            TXdescripcion.requestFocus();
        }else if(Double.parseDouble(TXcantidad.getText().trim())==0){
            ctrl.mensaje.MensajeAlerta("La cantidad debe ser mayor a 0");
            ctrl.Maracar(TXcantidad);
        }else if(Double.parseDouble(TXpUnitario.getText().trim())==0){
            ctrl.mensaje.MensajeAlerta("El precio Unitario debe ser mayor a 0");
            ctrl.Maracar(TXpUnitario);
        }else if(Double.parseDouble(TXptotal.getText().trim())==0){
            ctrl.mensaje.MensajeAlerta("El total debe ser mayor a 0");
            ctrl.Maracar(TXptotal);
        }else if(CBservicio.getSelectedIndex()==-1){
            ctrl.mensaje.MensajeAlerta("Seleccione el tipo de servicio.");
            CBservicio.requestFocus();
        }else if(LBnroservicio.getText().trim().length()==0){
            ctrl.mensaje.MensajeAlerta("No se seleccion√≥ un Nro de servicio.");
            this.dispose();
        }else{
            String npartida=TXnroPartida.getText().trim();
            String descripcion=TXdescripcion.getText().trim();
            String cantidad=TXcantidad.getText().trim();
            String puni=TXpUnitario.getText().trim();
            String ptotal=TXptotal.getText().trim();
            String idorden=LBnroservicio.getText().trim();
            String cb=ctrl.ObtenerCodigo("tipo_servicio", "descripcion", CBservicio.getSelectedItem().toString().trim(), 1);
            ctrl.GrabarData("detalle_servicio", "nro_partida,descripcion_servicio,cantidad,preciounitario,preciototal,idordenservicio,idtiposervicio", 
                    "'"+npartida+"','"+descripcion+"','"+cantidad+"','"+puni+"','"+ptotal+"','"+idorden+"','"+cb+"'");
            Limpiar();
        }
    }

    private void Calcular() {
        double a,b,resp;
        a=Double.parseDouble(TXcantidad.getText().trim());
        b=Double.parseDouble(TXpUnitario.getText().trim());
        resp=a*b;
        TXptotal.setText(String.valueOf(resp));
    }
    private void VerTabla(){
        ctrl.VisualizarDataEscogidaEnTabla("detalle_servicio_iddetalleservicio,detalle_servicio_idordenservicio,detalle_servicio_descripcion_servicio,detalle_servicio_cantidad,detalle_servicio_preciounitario,detalle_servicio_preciototal", "v_detalle_servicio", jTable1, "where detalle_servicio_idordenservicio='"+LBnroservicio.getText()+"'");
    }
    private void Limpiar(){
        TXnroPartida.setText("");
        TXdescripcion.setText("");
        CBservicio.setSelectedIndex(-1);
        TXcantidad.setText("0.0");
        TXpUnitario.setText("0.0");
        TXptotal.setText("0.0");
        VerTabla();       
    }
